export function buildInitialOpportunities() {
  let id = 1;
  const list = [];
  for (const [city, titles] of Object.entries(OPPORTUNITIES_BY_CITY)) {
    for (const title of titles) {
      list.push(buildOpportunity(id++, city, title));
    }
  }
  return list;
}
