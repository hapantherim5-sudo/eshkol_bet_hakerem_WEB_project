function detectCategory(title) {
  const t = title.toLowerCase();
  if (/כדור|טניס|שחי|התעמלות|זומבה|הוקי|ג'ודו|קראטה|טאיק|קונג|קאפויר|ספורט|אופניים|טריאתלון|בייסבול|כדור עף|כדורשת|נינג'ה|ברייק|ביודנס|היפ הופ|ריקוד|מחול|בלט|אקרובט|יוגה|סייף|חץ|גליש|טיול/.test(t)) return 'sport';
  if (/אומנות|ציור|מוזיק|מוסיק|תאטרון|קרמיק|יציר|גיטר|אורגנ|להק|מקהל|קונסרב|מנגן|קולנוע|צילום|בישול|בוצה|שיף/.test(t)) return 'art';
  if (/רובוט|מדע|סייבר|מייקר|שח מט|שחמט|מד"ב|מדצ|המדען|מפה ירוקה|ציפור/.test(t)) return 'science';
  if (/פסיכומטר|יע"ל|תעסוק|תיקון|העצמה|מנהיגות|הכשר/.test(t)) return 'workshops';
  if (/עברית|נוער|צהרון|בטחון לב|בסיכון|האוצר/.test(t)) return 'community';
  return 'community';
}

